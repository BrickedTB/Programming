package characterarrays;

import java.util.Arrays;

public class CharacterArrays {

    public static void main(String[] args) {
        char arr[] = new char[4];
        
        arr[0] = 'C';
        arr[1] = 'h';
        arr[2] = 'a';
        arr[3] = 'r';
        
        for (int i = 0; i < arr.length; i++)
        System.out.print(arr[i]);
        
    }
    
}
